namespace Licentia\Equity\Controller\Adminhtml\ExtraCosts;

/**
 * Class Delete
 *
 * @package Licentia\Panda\Controller\Adminhtml\ExtraCosts
 */
class Refresh extends \Licentia\Equity\Controller\Adminhtml\ExtraCosts
{

    /**
     * Delete action
     *
     * @return \Magento\Framework\Controller\ResultInterface
     */
    public function execute()
    {

        parent::execute();
        /** @var \Magento\Backend\Model\View\Result\Redirect $resultRedirect */
        $resultRedirect = $this->resultRedirectFactory->create();

        $this->pandaHelper->scheduleEvent('panda_update_sales_extra_costs');
        $this->messageManager->addSuccessMessage(__('Costs will be updated next time your cron runs'));

        return $resultRedirect->setPath('*/*/');
    }

}
